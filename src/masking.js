window.addEventListener('load', async () => {
  let _maskeeGain = 0.1;
  let _maskeeFrequency = 1000;
  let _maskerGain = 1;
  let maskeeGainNode = null;
  let maskeeSourceNode = null;
  let maskerGainNode = null;
  const audioProc = await setupAudio({
    init: (audioCtx) => {
      return {
        audioCtx: audioCtx,
        set maskeeGain(g) {
          if (maskeeGainNode) {
            maskeeGainNode.gain.linearRampToValueAtTime(g, audioProc.currentTime + 0.05);
          }
          _maskeeGain = g;
        },
        set maskeeFrequency(f) {
          if (maskeeSourceNode) {
            maskeeSourceNode.frequency.exponentialRampToValueAtTime(
              f,
              audioProc.currentTime + 0.05,
            );
          }
          _maskeeFrequency = f;
        },
        set maskerGain(g) {
          if (maskerGainNode) {
            maskerGainNode.gain.linearRampToValueAtTime(g, audioProc.currentTime + 0.05);
          }
          _maskerGain = g;
        },
      };
    },
    setup: (proc, src, sink) => {
      maskeeSourceNode = proc.audioCtx.createOscillator();
      maskeeSourceNode.type = 'sine';
      maskeeSourceNode.frequency.value = _maskeeFrequency;
      maskeeSourceNode.start();
      maskeeGainNode = proc.audioCtx.createGain();
      maskeeGainNode.gain.value = _maskeeGain;
      maskerGainNode = proc.audioCtx.createGain();
      maskerGainNode.gain.value = _maskerGain;

      maskeeSourceNode.connect(maskeeGainNode);
      maskeeGainNode.connect(sink);
      src.connect(maskerGainNode);
      maskerGainNode.connect(sink);
    },
    teardown: (/*proc*/) => {
      if (maskeeSourceNode) {
        maskeeSourceNode.disconnect();
        maskeeSourceNode = null;
      }
      if (maskeeGainNode) {
        maskeeGainNode.disconnect();
        maskeeGainNode = null;
      }
      if (maskerGainNode) {
        maskerGainNode.disconnect();
        maskerGainNode = null;
      }
    },
  });
  audioProc.proc.maskeeGain = 0.01;

  setupPlayerControls(audioProc, [
    { type: 'noise', filter: h1000, label: 'Noise @ 1kHz' },
    { type: 'noise', filter: h550, label: 'Noise @ 550Hz' },
    { type: 'sine' },
  ]);

  const graph = new SignalGraph(audioProc, document.getElementById('funccanvas'));
  const cblinear = document.getElementById('linear');
  cblinear.checked = false;
  cblinear.onchange = function (event) {
    graph.freqLinear = event.target.checked;
  };
  const setDrawWave = (b) => {
    graph.drawWave = b;
    cblinear.style.visibility = cblinear.labels[0].style.visibility = b
      ? 'hidden'
      : 'visible';
  };
  document.getElementById('spectrum').onchange = (event) => {
    setDrawWave(!event.target.checked);
  };
  document.getElementById('waveform').onchange = (event) => {
    setDrawWave(event.target.checked);
  };

  document.getElementById('maskee').checked = true;
  document.getElementById('maskee').onchange = function (event) {
    if (event.target.checked) {
      audioProc.proc.maskeeGain = Math.pow(
        10,
        document.getElementById('maskeegain').value / 20,
      );
    } else {
      audioProc.proc.maskeeGain = 0;
    }
  };
  document.getElementById('maskeegain').value = '-40';
  document.getElementById('maskeegain').oninput = function (event) {
    if (document.getElementById('maskee').checked) {
      audioProc.proc.maskeeGain = Math.pow(10, event.target.value / 20);
    }
    event.target.labels[0].textContent = `${event.target.value} dB`;
  };
  document.getElementById('maskeefrequency').value = '1000';
  document.getElementById('maskeefrequency').oninput = function (event) {
    audioProc.proc.maskeeFrequency = event.target.value;
    event.target.labels[0].textContent = `${event.target.value} Hz`;
  };
  document.getElementById('masker').checked = true;
  document.getElementById('masker').onchange = function (event) {
    if (event.target.checked) {
      audioProc.proc.maskerGain = 1;
    } else {
      audioProc.proc.maskerGain = 0;
    }
  };
});
